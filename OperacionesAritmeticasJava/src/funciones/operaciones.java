public class operaciones {

    //method sum
    public int sum(int a, int b) {
        return a + b;
    }
    //method rest

    public int rest(int a, int b) {
        return a - b;
    }
    //method multiply

    public int multiply(int a, int b) {
        return a * b;
    }
    //method divide

    public float divide(int a, int b) {
        return a / b;
    }
}
